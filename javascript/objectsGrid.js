function drawObjectTiles(){
  var playerX = player1.x  / 50;
  var playerY = player1.y / 50;
  //console.log(playerY + " " + playerX); //succesful
  var startArraySearchX = playerX - 10;
  var endArraySearchX = playerX + 9;

  var startArraySearchY = playerY - 5;
  var endArraySearchY = playerY + 4;
  if(endArraySearchY > 39){
    endArraySearchY = 39;
  }

  var whiteSpaceY=0;
  var whiteSpaceX=0;
  
  for(var rowCount = startArraySearchY; rowCount <= endArraySearchY; rowCount++){
    if(rowCount>= 0){
      whiteSpaceX = 0;
      for(var columnCount = startArraySearchX; columnCount <= endArraySearchX; columnCount++){
        if(columnCount>=0){
          if(gadflyNPCList.includes(objectGrid[rowCount][columnCount])) // drawing active gadflys
          { 
            objectGrid[rowCount][columnCount].update();
            drawNPC(objectGrid[rowCount][columnCount],whiteSpaceX,whiteSpaceY);
          }
          if(goblinNPCList.includes(objectGrid[rowCount][columnCount]) ) // drawwing all goblins active
          { 
            objectGrid[rowCount][columnCount].update();
            if(objectGrid[rowCount][columnCount].health > 0){
              drawGoblin(objectGrid[rowCount][columnCount],whiteSpaceX,whiteSpaceY);
            }  
          } 
          // if statement checks object grid for every instance of a book
          if( bookList.includes(objectGrid[rowCount][columnCount]) && objectGrid[rowCount][columnCount].itemPickedUp == false){
            objectGrid[rowCount][columnCount].update();
            drawBook(objectGrid[rowCount][columnCount],whiteSpaceX,whiteSpaceY);
          }//end of code to dispaly and update books
          ///look for spear pressure plates
          if( spearPlateList.includes(objectGrid[rowCount][columnCount]) ){
            objectGrid[rowCount][columnCount].update();
            drawPlate(objectGrid[rowCount][columnCount],whiteSpaceX,whiteSpaceY);
          }
          //looks for arrows flying to kill player
          if( arrowsList.includes(objectGrid[rowCount][columnCount]) ){
            objectGrid[rowCount][columnCount].update();
            drawArrow(objectGrid[rowCount][columnCount],whiteSpaceX,whiteSpaceY);
          }
          //looks for zot spawners
          if( zotList.includes(objectGrid[rowCount][columnCount]) ){
            objectGrid[rowCount][columnCount].update();
            drawZot(objectGrid[rowCount][columnCount],whiteSpaceX,whiteSpaceY);
          }
          //lopoking for moving stones
          if( stoneList.includes(objectGrid[rowCount][columnCount]) ){
            objectGrid[rowCount][columnCount].update();
            drawStone(objectGrid[rowCount][columnCount],whiteSpaceX,whiteSpaceY);
          }
          //check for entranddoors to draw and key doors
          if( entranceDoorList.includes(objectGrid[rowCount][columnCount]) ){
            //objectGrid[rowCount][columnCount].update();
            drawDoor(objectGrid[rowCount][columnCount],whiteSpaceX,whiteSpaceY);
          }
          //check for key doors
          if( keyDoorList.includes(objectGrid[rowCount][columnCount]) ){
            objectGrid[rowCount][columnCount].update();
            drawDoor(objectGrid[rowCount][columnCount],whiteSpaceX,whiteSpaceY);
          }
          
          //checks for trees
          if( treeList.includes(objectGrid[rowCount][columnCount]) ){
            //objectGrid[rowCount][columnCount].update();
            drawTree(objectGrid[rowCount][columnCount],whiteSpaceX,whiteSpaceY);
          }
          //check and draw keys
          if( keyList.includes(objectGrid[rowCount][columnCount]) ){
            objectGrid[rowCount][columnCount].update();
            drawKey(objectGrid[rowCount][columnCount],whiteSpaceX,whiteSpaceY);
          }
        }//end of search
        else if(columnCount < 0){
        //console.log("skip regular function and add one to columnWhiteSpace");
        }
        whiteSpaceX++;
      }//end of inner for
    }//end of row count if
    else if(rowCount<0){
    //console.log("skip regular function and add one to rowWhiteSpace");

    }
    //console.log("This number should cap at 10: " + whiteSpaceY);
    whiteSpaceY++;
  }//end of for

}//end of drawing of objects on top of map